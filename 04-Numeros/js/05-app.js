let puntaje = 5;

puntaje++; // Aumenta en uno la siguiente vez que sea llamado // puntaje = 5
++puntaje; // Aumenta en uno inmediamante // puntaje = 6

puntaje--; // Disminuye en uno la siguiente vez que sea llamado // puntaje = 5
--puntaje; // Disminuye en uno inmediamante // puntaje = 4

puntaje += 3; // Le suma 3
puntaje -= 3; // Le resta 3
puntaje *= 2; // Le multiplica 2
puntaje /= 2; // Le divide 2